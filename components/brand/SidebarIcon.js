const ROUTE_ICON_MAP = {
  panel: "dashboard",
  boty: "bot",
  historia: "history",
  prompt: "prompt",
  wyglad: "appearance",
  pliki: "files",
  instalacja: "install",
};

const ROUTE_GRADIENT_MAP = {
  panel: "primary",
  boty: "green",
  historia: "orange",
  prompt: "purple",
  wyglad: "purple",
  pliki: "blue",
  instalacja: "primary",
};

const buildInlineSvg = (name) => {
  const markup = ICON_SVG_MAP[name] || ICON_SVG_MAP.settings;
  const template = document.createElement("template");
  template.innerHTML = markup.trim();
  return template.content.firstElementChild;
};

export const createSidebarIcon = ({ name, alt = "" } = {}) => {
  const wrapper = document.createElement("div");
  wrapper.className = "sidebar-icon";
  wrapper.setAttribute("aria-hidden", "true");
  if (alt) wrapper.setAttribute("aria-label", alt);
  wrapper.appendChild(buildInlineSvg(name || "settings"));
  return wrapper;
};

const injectSidebarIcon = (item, iconName) => {
  if (!item || !iconName) return;

  const existing = item.querySelector(".sidebar-icon");
  if (existing) {
    if (!existing.querySelector("svg")) {
      existing.innerHTML = "";
      existing.appendChild(buildInlineSvg(iconName));
    }
    return;
  }

  const legacyIcon = item.querySelector(".nav-ico");
  const label = item.querySelector(".label");
  const icon = createSidebarIcon({ name: iconName });

  if (legacyIcon) {
    legacyIcon.remove();
  }

  if (label) {
    item.insertBefore(icon, label);
  } else {
    item.prepend(icon);
  }
};

export const mountSidebarIcons = (root) => {
  if (!root) return;

  const navItems = root.querySelectorAll(".nav-item[data-route]");
  navItems.forEach((item) => {
    const route = item.dataset.route;
    const iconName = ROUTE_ICON_MAP[route];
    if (!iconName) return;

    item.classList.add("sidebar-item");
    item.dataset.gradient = ROUTE_GRADIENT_MAP[route] || "primary";
    injectSidebarIcon(item, iconName);
  });
};
